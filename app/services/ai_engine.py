class AIEngine:

    def __init__(self):
        self.api_key = settings.GROQ_API_KEY
        self.model = settings.GROQ_MODEL
        self.base_url = "https://api.groq.com/openai/v1"

    def _build_system_prompt(self, products: List[Dict[str, Any]]) -> str:
        return MARIA_SYSTEM_PROMPT

    @retry(
        stop=stop_after_attempt(settings.GROQ_MAX_RETRIES),
        wait=wait_exponential(multiplier=settings.GROQ_RETRY_DELAY, min=1, max=10),
        retry=retry_if_exception_type((httpx.RequestError, httpx.HTTPStatusError)),
    )
    async def _call_groq_api(self, messages: List[Dict[str, str]]) -> str:
        async with httpx.AsyncClient(timeout=30.0) as client:
            response = await client.post(
                f"{self.base_url}/chat/completions",
                headers={
                    "Authorization": f"Bearer {self.api_key}",
                    "Content-Type": "application/json"
                },
                json={
                    "model": self.model,
                    "messages": messages,
                    "temperature": 0.3,
                    "max_tokens": 1024,
                    "response_format": {"type": "json_object"}
                }
            )
            response.raise_for_status()
            data = response.json()
            return data["choices"][0]["message"]["content"]

    async def process_message(
        self,
        message: str,
        conversation_history: List[Dict[str, str]],
        current_state: str,
        context_data: Dict[str, Any],
        products: List[Dict[str, Any]]
    ) -> AIResponse:
        system_prompt = self._build_system_prompt(products)

        messages = [
            {"role": "system", "content": system_prompt},
            {"role": "system", "content": f"الحالة الحالية: {current_state}\nالبيانات المستخرجة حالياً: {json.dumps(context_data, ensure_ascii=False)}"}
        ]

        for msg in conversation_history[-10:]:
            messages.append({
                "role": "user" if msg.get("role") == "user" else "assistant",
                "content": msg.get("content", "")
            })

        messages.append({"role": "user", "content": message})

        try:
            content = await self._call_groq_api(messages)
            result = json.loads(content)
            return AIResponse(**result)
        except Exception as e:
            return AIResponse(
                intent="faq",
                response="عذراً، واجهت مشكلة في معالجة طلبك. الرجاء المحاولة مرة أخرى لاحقاً، أو اكتب 'موظف' للتحدث مع أحد موظفينا.",
                needs_human=False
            )


ai_engine = AIEngine()
